fun main (){
//    Type Data Bilangan real
    var byteData: Byte = 23
    var Integer: Int = 100
    var shortData : Short = 2000
    var longData : Long = 300000L
    println(byteData)
    println(Integer)
    println(shortData)
    println(longData)

//    Type Data bilangan pecahan
    var floatData: Float = 10.23F
    var doubleData : Double = 30.2827
    println(floatData)
    println(doubleData)

//    Konfersi type data
    var number: Double = 29.330
    var numberInt: Int = number.toInt()
    print(numberInt)



}